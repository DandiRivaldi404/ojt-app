    {
        $ValidatedData = $request->validate([
            'nama_lengkap' => '',
            'jabatan' => '',
            'lokasi_id' => ''
        ]);

        Koordinator::where('id_koordinator', $koordinator->id_koordinator)->update($ValidatedData);
