     */
    public function update(Request $request,Mahasiswa $mahasiswa)
    {
        $ValidatedData = $request->validate([
            'nama_mahasiswa' => '',
            'semester' => '',
            'lokasi_id' => ''
        ]);

        Mahasiswa::where('nim', $mahasiswa->nim)->update($ValidatedData);
